
public class Main {
	public static void main(String[] args) {
		int integer      = Integer.SIZE;
		int longInteger  = Long.SIZE;
		int shortInteger = Short.SIZE;
		int floatNumber  = Float.SIZE;
		int doubleNumber = Double.SIZE;
		int character    = Character.SIZE;
		
		System.out.printf("The size of int    : %d bit\n" , integer);
		System.out.printf("The size of long   : %d bit\n" , longInteger);
		System.out.printf("The size of short  : %d bit\n" , shortInteger);
		System.out.printf("The size of float  : %d bit\n" , floatNumber);
		System.out.printf("The size of double : %d bit\n" , doubleNumber);
		System.out.printf("The size of char   : %d bit\n" , character);
				
	}
}
